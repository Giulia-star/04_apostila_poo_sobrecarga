public class Impressora{
    public void exibir(float ex1){
        System.out.println(ex1);
    }
    public void exibir(float f1, float b){
        System.out.println(f1+"," +b);
    }
    public void exibir(float v, String s){
        System.out.println(v+","+s);
    }
    public void exibir(String a, float i){
        System.out.println(a+ "," +i);
    }
    public void exibir(String c, String d, String e){
        System.out.println(c+ "," +d +"," +e);
    }
    public void exibir(int m1, int y, int l){
        System.out.println(m1+ ","+y+","+l);
    }

}